namespace Syncfusion.Maui.Toolkit.Internals
{
    /// <summary>
    /// Defines the various modes of pan gesture
    /// </summary>
    public enum PanMode
    {
        /// <summary>
        /// Specifies pan is disabled in both the directions.
        /// </summary>
        None,
        /// <summary>
        /// Specifies pan is enabled only in the horizontal direction.
        /// </summary>
        Horizontal,
        /// <summary>
        /// Specifies pan is enabled only in the horizontal direction.
        /// </summary>
        Vertical,
        /// <summary>
        /// Specifies pan is enabled in both the directions.
        /// </summary>
        Both,
    }
}