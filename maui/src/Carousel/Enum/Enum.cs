namespace Syncfusion.Maui.Toolkit.Carousel
{
	/// <summary>
	/// Represents the various display modes for the <see cref="SfCarousel"/>.
	/// </summary>
	public enum ViewMode
    {
		/// <summary>
		/// The carousel items are arranged in a default 3D layout.
		/// </summary>
		Default,

		/// <summary>
		/// A view mode that displays content in a linear format arranged horizontally.
		/// </summary>
		Linear
	}

	/// <summary>
	/// Represents the modes of swipe movement for manipulating items.
	/// </summary>
	public enum SwipeMovementMode
    {
		/// <summary>
		/// Indicates that a single item should be moved.
		/// </summary>
		SingleItem,

		/// <summary>
		/// Indicates that multiple items should be moved.
		/// </summary>
		MultipleItems
	}
}
